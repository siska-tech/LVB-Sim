//! μMML / mmml パーサモジュール
//!
//! Protodome の mmml フォーマットを解析して Sequence に変換する。
//!
//! フォーマット仕様:
//! - `@` : チャンネル / マクロ区切り (最初の 4 つがチャンネル A-D)
//! - `%` : 行コメント
//! - 音符: c, c+, d, d+, e, f, f+, g, g+, a, a+, b
//! - 休符: r
//! - 長さ: 1, 2, 4, 8, 16, 32, 64, 128 (付点: .)
//! - オクターブ: o1-o5, < (下げる), > (上げる)
//! - 音量: v0-v8
//! - テンポ: t1-t255
//! - ループ: [n ... ]
//! - マクロ呼び出し: m1-m255
//! - タイ: &

pub mod parser;
